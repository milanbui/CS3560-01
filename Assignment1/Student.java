package CS3560_Assignment1;

/****************************************************************************************
 * sTUDENT Class
 *    This class represents an Student object and implements the StudentInfo interface.
 *    It manages 1 attribute: id.
 ****************************************************************************************/
public class Student implements StudentInfo
{
	// Instance Variables
	private String id;    // Student ID
	
	
	/************************************************************************************
	 * Default Constructor
	 * ----------------------------------------------------------------------------------
	 * Initializes id
	 * ----------------------------------------------------------------------------------
	 * Parameters:
	 *     none
	 * Return:
	 *     none
	 ************************************************************************************/
	public Student(){
		this.id = "";
	}
	
	
	/************************************************************************************
	 * Constructor
	 * ----------------------------------------------------------------------------------
	 * Initializes id to given String
	 * ----------------------------------------------------------------------------------
	 * Parameters:
	 *     id (String) - a student's ID
	 * Return:
	 *     none
	 ************************************************************************************/
	public Student(String id) // IN - student's id
	{
		this.id = id;
	}
	
	
	/************************************************************************************
	 * Mutator | setID | Student interface method
	 * ----------------------------------------------------------------------------------
	 * Sets the student's ID to given string.
	 * ----------------------------------------------------------------------------------
	 * Parameters:
	 *     id (String) - student's ID
	 * Returns:
	 *     none
	 ************************************************************************************/
	@Override
	public void setID(String id) // IN - student's ID
	{
		this.id = id;
	}
	
	/************************************************************************************
	 * Accessor | getID
	 * ----------------------------------------------------------------------------------
	 * Returns studen't id
	 * ----------------------------------------------------------------------------------
	 * Parameters:
	 *     none
	 * Return:
	 *     id (String) - student's ID
	 ************************************************************************************/
	public String getID(){
		return id;
	}
	

}
